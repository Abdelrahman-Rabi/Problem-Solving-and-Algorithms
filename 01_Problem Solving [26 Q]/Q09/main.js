/* Longest Sequence */

/*  
Write a function that returns the length of the longest sequence of 1s in an array of 1s and 0s.
*/

function longestSequence(arr) {
  let result = [];
  let sum = 0;
  for (let i = 0; i < arr.length; i++) {
    if (arr[i] === 1) {
      sum += 1;
    } else {
      result.push(sum);
      sum = 0;
    }
  }
  result.push(sum);
  return Math.max(...result);
}

